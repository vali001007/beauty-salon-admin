function parseFieldLine(line: string): PrismaField | null {
  const [name, rawType, ...rest] = line.split(/\s+/);
  if (!name || !rawType) return null;
  const attributes = rest.join(' ');
  const optional = rawType.endsWith('?');
  const list = rawType.endsWith('[]');
  const type = rawType.replace(/[?\[\]]/g, '');
  return {
    name,
    rawType,
    type,
    attributes,
    optional,
    list,
    id: attributes.includes('@id'),
    unique: attributes.includes('@unique'),
    indexed: attributes.includes('@index'),
    relation: false,
  };
}

function parseModelIndexFields(line: string) {
  const matched = line.match(/\[(.*?)\]/);
  if (!matched) return [];
  return [
    matched[1]
      .split(',')
      .map((item) => item.trim().replace(/\(.+\)$/, ''))
      .filter(Boolean),
  ];
}

function parseRelationArgs(attributes: string) {
  const relation = attributes.match(/@relation\((.*?)\)/);
  if (!relation) return { fields: [] as string[], references: [] as string[] };
  return {
    fields: parseRelationArray(relation[1], 'fields'),
    references: parseRelationArray(relation[1], 'references'),
  };
}

function parseRelationArray(text: string, key: string) {
  const matched = text.match(new RegExp(`${key}:\\s*\\[(.*?)\\]`));
  if (!matched) return [];
  return matched[1]
    .split(',')
    .map((item) => item.trim())
    .filter(Boolean);
}

function countByFixed<T, K extends string>(items: T[], keys: readonly K[], keyFn: (item: T) => K): Record<K, number> {
  const result = Object.fromEntries(keys.map((key) => [key, 0])) as Record<K, number>;
  for (const item of items) {
    const key = keyFn(item);
    result[key] = (result[key] ?? 0) + 1;
  }
  return result;
}

function normalizeOverrideRelationType(value: string): KnowledgeGraphEdgeType | null {
  const relationType = String(value ?? '').trim().toUpperCase();
  return EDGE_TYPES.includes(relationType as KnowledgeGraphEdgeType) ? (relationType as KnowledgeGraphEdgeType) : null;
}

function clampConfidence(value: number) {
  if (!Number.isFinite(value)) return 1;
  return Math.max(0, Math.min(1, value));
}

function normalizeTerm(term: string) {
  return String(term || '')
    .trim()
    .toLowerCase()
    .replace(/\s+/g, '');
}

function stableId(value: string) {
  return normalizeTerm(value).replace(/[^a-z0-9\u4e00-\u9fa5:._-]+/g, '-');
}

function domainId(domain: string) {
  return `domain:${stableId(domain || 'unknown')}`;
}

function businessObjectId(objectType: string) {
  return `business-object:${stableId(objectType)}`;
}

function modelId(model: string) {
  return `data-model:${stableId(model)}`;
}

function fieldId(model: string, field: string) {
  return `field:${stableId(model)}.${stableId(field)}`;
}

function capabilityId(id: string) {
  return `capability:${stableId(id)}`;
}

function endpointId(method: string, path: string) {
  return `capability:endpoint:${stableId(method)}:${stableId(path)}`;
}

function routeId(path: string) {
  return `capability:route:${stableId(path)}`;
}

function actionId(action: string) {
  return `action:${stableId(action)}`;
}

function wordId(term: string) {
  return `word:${stableId(term)}`;
}

function permissionId(permission: string) {
  return `permission:${stableId(permission)}`;
}

function edgeId(type: KnowledgeGraphEdgeType, from: string, to: string, label?: string) {
  return `${type}:${from}->${to}${label ? `:${stableId(label)}` : ''}`;
}

function isSensitiveField(field: string) {
  return /phone|mobile|idcard|password|token|secret|openid|unionid|address/i.test(field);
}

function chunkString(value: string, chunkSize: number) {
  const chunks: string[] = [];
  for (let index = 0; index < value.length; index += chunkSize) {
    chunks.push(value.slice(index, index + chunkSize));
  }
  return chunks;
}
