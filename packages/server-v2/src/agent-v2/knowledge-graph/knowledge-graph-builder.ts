export function buildAgentV2KnowledgeGraph(input: BuildAgentV2KnowledgeGraphInput): KnowledgeGraphSnapshot {
  const schemaHash = computeSchemaHash(input.schema);
  const models = parsePrismaModels(input.schema);
  const modelNames = new Set(models.map((model) => model.name));
  const graph: GraphAccumulator = { nodes: new Map(), edges: new Map() };

  addDomainNodes(graph, input.generatedAt);
  addActionIntentNodes(graph, input.generatedAt);
  addPrismaNodes(graph, models, modelNames, input);
  addBusinessObjectNodes(graph, BUSINESS_OBJECT_CATALOG, modelNames, input);
  addSemanticTermNodes(graph, input.semanticTerms, input.generatedAt);
  addManifestNodes(graph, input.manifests, modelNames, input);
  addControllerEndpointNodes(graph, input);
  addFrontendRouteNodes(graph, input);
  addManifestExcludeEdges(graph, input.manifests, input.generatedAt);
  const manualOverrideMergeDetails = addManualOverrideEdges(graph, input.manualOverrides ?? [], input.generatedAt);

  const nodes = [...graph.nodes.values()].sort((a, b) => a.id.localeCompare(b.id));
  const edges = [...graph.edges.values()].sort((a, b) => a.id.localeCompare(b.id));
  const summary = buildSummary(input.generatedAt, schemaHash, nodes, edges, input.manifests.length);
  const report = buildCoverageReport(input.generatedAt, schemaHash, summary, nodes, edges, models, input.manifests, input.manualOverrides ?? [], manualOverrideMergeDetails);

  return {
    generatedAt: input.generatedAt,
    schemaHash,
    summary,
    nodes,
    edges,
    report,
  };
}

