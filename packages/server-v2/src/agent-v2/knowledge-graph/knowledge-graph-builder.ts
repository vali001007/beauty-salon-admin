function addNode(graph: GraphAccumulator, node: KnowledgeGraphNode) {
  const current = graph.nodes.get(node.id);
  if (!current || current.confidence < node.confidence) {
    graph.nodes.set(node.id, { ...current, ...node, properties: { ...(current?.properties ?? {}), ...(node.properties ?? {}) } });
  }
}

function addEdge(
  graph: GraphAccumulator,
  edge: Omit<KnowledgeGraphEdge, 'id'>,
) {
  const id = edgeId(edge.type, edge.from, edge.to, edge.label);
  if (graph.edges.has(id)) return;
  graph.edges.set(id, { ...edge, id });
}

function addWordSynonymEdge(
  graph: GraphAccumulator,
  term: string,
  targetId: string,
  generatedAt: string,
  sourcePath: string | undefined,
  source: KnowledgeGraphSource,
  edgeType: Extract<KnowledgeGraphEdgeType, 'SYNONYM_OF' | 'TRIGGERS'> = 'SYNONYM_OF',
) {
  const normalized = normalizeTerm(term);
  if (!normalized) return;
  addNode(graph, {
    id: wordId(term),
    type: 'Word',
    name: term,
    displayName: term,
    source,
    sourcePath,
    confidence: source === 'manifest' ? 0.9 : 0.96,
    updatedAt: generatedAt,
  });
  addEdge(graph, {
    type: edgeType,
    from: wordId(term),
    to: targetId,
    label: term,
    source,
    sourcePath,
    confidence: source === 'manifest' ? 0.9 : 0.96,
    updatedAt: generatedAt,
  });
}

