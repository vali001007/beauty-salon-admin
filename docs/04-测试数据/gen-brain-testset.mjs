// 用互质步长错开各变量，最大化唯一组合
const fill = (s, i) =>
  s
    .replace(/\{t2\}/g, TIME[(i * 7 + 3) % TIME.length])
    .replace(/\{t\}/g, TIME[(i * 3 + 1) % TIME.length])
    .replace(/\{n\}/g, BEA[(i * 5 + 2) % BEA.length])
    .replace(/\{c\}/g, CUS[(i * 7 + 1) % CUS.length])
    .replace(/\{p\}/g, PROJ[(i * 4 + 2) % PROJ.length])
    .replace(/\{pr\}/g, PROD[(i * 7 + 1) % PROD.length])
    .replace(/\{card\}/g, CARD[(i * 3 + 1) % CARD.length])
    .replace(/\{lv\}/g, LV[i % LV.length])
    .replace(/\{d\}/g, String(50 + (i * 37) % 900));

const rows = [];
let idCounter = 0;
const seen = new Set();
let GCUR = 0; // 全局游标：跨模板持续推进，让真实实体（客户/产品/项目）在问题集中充分铺开

function esc(s) {
  const str = String(s ?? '');
  return /[",\n]/.test(str) ? '"' + str.replace(/"/g, '""') + '"' : str;
}

function addRow(domain, role, type, difficulty, q, target, notes, saltForFn) {
  idCounter += 1;
  const id = 'BQ' + String(idCounter).padStart(4, '0');
  const tgt = typeof target === 'function' ? target(saltForFn) : target;
  const nt = typeof notes === 'function' ? notes(saltForFn) : notes;
  rows.push([id, domain, role, type, difficulty, esc(q), esc(tgt), esc(nt)].join(','));
}

/**
 * 生成 n 条唯一问题：
 * 1) 为每个模板收集它能产出的全部唯一句子（连续 j 展开，步长与基数互质→周期最大）；
 * 2) round-robin 均匀轮取，过全局 seen 去重，直到凑够 n。
 * 彻底规避步长陷阱与低基数塌缩。
 */
function gen(domain, role, type, difficulty, n, templates, target, notes = '') {
  const m = templates.length;
  const CAP = 80; // 单模板候选上限（远大于任何配额）
  // 用全局游标 GCUR 填充变量：不同模板从错开的实体索引起步，覆盖面更广
  const pools = templates.map((tpl) => {
    const local = new Set();
    let miss = 0;
    for (let k = 0; k < 1200 && local.size < CAP && miss < 240; k += 1) {
      const before = local.size;
      local.add(fill(tpl, GCUR));
      GCUR += 1;
      miss = local.size > before ? 0 : miss + 1; // 连续无新增达阈值即停（该模板变量已耗尽）
    }
    return [...local];
  });

  let produced = 0;
  let saltSeq = 0;
  let round = 0;
  while (produced < n) {
    let progressed = false;
    for (let ti = 0; ti < m && produced < n; ti += 1) {
      const q = pools[ti][round];
      if (q === undefined) continue;
      progressed = true;
      if (seen.has(q)) continue;
      seen.add(q);
      saltSeq += 1;
      addRow(domain, role, type, difficulty, q, target, notes, saltSeq);
      produced += 1;
    }
    round += 1;
    if (!progressed) break; // 所有模板候选耗尽
  }

  if (produced < n) {
    const cap = pools.reduce((s, p) => s + p.length, 0);
    throw new Error(`模板容量不足: ${domain}/${type} 需${n}, 候选上限${cap}, 实得${produced}`);
  }
}

